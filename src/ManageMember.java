private void loadMembersTable() {
    try {
        List<Member> members = memberDAO.getAllMembers();
        DefaultTableModel model = new DefaultTableModel(
                new Object[]{"MemberID", "Name", "Email", "Role"}, 0);

        for (Member m : members) {
            model.addRow(new Object[]{
                    m.getMemberID(),
                    m.getName(),
                    m.getEmail(),
                    m.getRole()
            });
        }
        tblMember.setModel(model);
    } catch (Exception e) {
        e.printStackTrace();
        JOptionPane.showMessageDialog(this,
                "Error loading members:\n" + e.getMessage(),
                "Error", JOptionPane.ERROR_MESSAGE);
    }
}
private void clearFields() {
    txtMemberID.setText("");
    txtName.setText("");
    txtEmail.setText("");
    txtPassword.setText("");
    jcbRoles.setSelectedIndex(0);
    tblMember.clearSelection();
}
