    private void jbDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbDeleteActionPerformed
    Integer id = getMemberIdFromField();
    if (id == null) {
        JOptionPane.showMessageDialog(this,
                "Select a member to delete.",
                "Validation", JOptionPane.WARNING_MESSAGE);
        return;
    }

    String username = txtName.getText().trim();

    int confirm = JOptionPane.showConfirmDialog(
            this,
            "Are you sure you want to delete this member?",
            "Confirm Delete",
            JOptionPane.YES_NO_OPTION);

    if (confirm != JOptionPane.YES_OPTION) {
        return;
    }

    try {
        if (!username.isEmpty()) {
            accountDAO.deleteByUsername(username);
        }
        memberDAO.deleteMember(id);

        JOptionPane.showMessageDialog(this, "Member deleted.");
        clearFields();
        loadMembersTable();
    } catch (Exception e) {
        e.printStackTrace();
        JOptionPane.showMessageDialog(this,
                "Error deleting member:\n" + e.getMessage(),
                "Error", JOptionPane.ERROR_MESSAGE);
    }
    }//GEN-LAST:event_jbDeleteActionPerformed

    private void jbUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbUpdateActionPerformed
        Integer id = getMemberIdFromField();
    if (id == null) {
        JOptionPane.showMessageDialog(this,
                "Select a member to update.",
                "Validation", JOptionPane.WARNING_MESSAGE);
        return;
    }

    String name  = txtName.getText().trim();
    String email = txtEmail.getText().trim();
    String role  = jcbRoles.getSelectedItem().toString();

    if (name.isEmpty() || email.isEmpty()) {
        JOptionPane.showMessageDialog(this,
                "Name and Email are required.",
                "Validation", JOptionPane.WARNING_MESSAGE);
        return;
    }

    try {
        Member m = new Member();
        m.setMemberID(id);
        m.setName(name);
        m.setEmail(email);
        m.setRole(role);

        memberDAO.updateMember(m);
        JOptionPane.showMessageDialog(this, "Member updated.");
        clearFields();
        loadMembersTable();
    } catch (Exception e) {
        e.printStackTrace();
        JOptionPane.showMessageDialog(this,
                "Error updating member:\n" + e.getMessage(),
                "Error", JOptionPane.ERROR_MESSAGE);
    }
    }//GEN-LAST:event_jbUpdateActionPerformed

